public class operators {
    public static void main(String[] args) {
        System.out.println('a'+'b');        // adding ascii value
        System.out.println("a"+"b");        // adding string
        System.out.println('a'+3);          // adding ascii value + 3
        System.out.println((char)('a'+3));  // here char a + 3 is d.
        System.out.println("a"+1);          //when inteeger is added to a string it is converted to a wrapper class integer

    }
}
